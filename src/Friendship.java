
public class Friendship {
	
	int person1;
	int person2;
	String time;
	
	
	
	public Friendship(int person1, int person2, String time) {
		this.person1 = person1;
		this.person2 = person2;
		this.time = time;
	}



	public int getPerson1() {
		return person1;
	}



	public int getPerson2() {
		return person2;
	}



	public String getTime() {
		return time;
	}
	
	
	
}
